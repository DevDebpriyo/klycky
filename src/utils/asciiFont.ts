// Renders large text
export function renderLargeText(text: string): string[] {
  const result: string[] = ['', '', '', ''];
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const lines = FONT_MAP[char] || FONT_MAP[' '];
    for (let j = 0; j < 4; j++) {
      result[j] += lines[j];
    }
  }
  return result;
}
